let currentRouteLayer = null;

// Function to display the selected route
function displayRoute(routeId) {
  if (currentRouteLayer) {
    map.removeLayer(currentRouteLayer); // Remove the previous route
  }
  const routeData = routes[routeId];
  if (routeData) {
    currentRouteLayer = L.geoJSON(routeData).addTo(map);
    map.fitBounds(currentRouteLayer.getBounds()); // Zoom to the route
  }
}

// Event listener for the dropdown
document.getElementById('route-dropdown').addEventListener('change', function (e) {
  const selectedRoute = e.target.value;
  if (selectedRoute) {
    displayRoute(selectedRoute);
  }
});
